// Operadores relacionais

// Igual a (==)
let igual = 5 == 5;
console.log(igual);

//  Idêntico a (===)
let identico = 5 === '5';
console.log(identico);

//  Idêntico a (===)
let identico2 = 5 === "5";
console.log(identico2);

//  Diferente de (!=)
let diferente = 5 != 5;
console.log(diferente);

//  Não idêntico a (!==)
let diferenteRestrito = 5 !== '5';
console.log("Diferença restrita: " + diferenteRestrito);

// Maior que (>)
let maiorQue = 5 > 3;
console.log('Maior que: ', maiorQue);

let menorQue = 5 < 3;
console.log('Menor que: ', menorQue);

let maiorIgual = 5 >= 3;
console.log('Maior igual: ', maiorQue);

let menorIgual = 5 <= 3;
console.log('Menor igual: ', maiorQue);